public class FormulasMath {
    
   public static int adição(int x, int y){
    return x+y;
   }
   public static int subtração(int x , int y)
   {
    return x-y;
   }
   public static int divisão(int x, int y)
   {
    return x/y;
   }
   public static int multiplação(int x, int y)
   {
    return x * y;
   }

}
